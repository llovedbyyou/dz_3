class WeatherCalendar {
    String month;
    int numberOfDays;
    int rainyDays;
    int year = 2020;
    boolean isRainyMonth;

    // Увеличить переменную rainyDays на единицу
    public void addRainyDay() {
        rainyDays += 1;
    }
    WeatherCalendar(String monthName, int monthNumberOfDays) {
        monthName = month;
        monthNumberOfDays = numberOfDays;
    }
}